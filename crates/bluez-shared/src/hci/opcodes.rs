/// Extract the OGF (Opcode Group Field) from an HCI opcode.
pub const fn ogf(opcode: u16) -> u16 {
    opcode >> 10
}

/// Extract the OCF (Opcode Command Field) from an HCI opcode.
pub const fn ocf(opcode: u16) -> u16 {
    opcode & 0x03FF
}

/// Construct an HCI opcode from OGF and OCF.
pub const fn opcode(ogf: u16, ocf: u16) -> u16 {
    (ogf << 10) | ocf
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_ogf_ocf() {
        // BT_HCI_CMD_RESET = 0x0C03 → OGF=0x03, OCF=0x03
        assert_eq!(ogf(BT_HCI_CMD_RESET), 0x03);
        assert_eq!(ocf(BT_HCI_CMD_RESET), 0x03);
        assert_eq!(opcode(0x03, 0x03), BT_HCI_CMD_RESET);

        // BT_HCI_CMD_LE_SET_EVENT_MASK = 0x2001 → OGF=0x08, OCF=0x01
        assert_eq!(ogf(BT_HCI_CMD_LE_SET_EVENT_MASK), 0x08);
        assert_eq!(ocf(BT_HCI_CMD_LE_SET_EVENT_MASK), 0x01);

        // BT_HCI_CMD_INQUIRY = 0x0401 → OGF=0x01, OCF=0x01
        assert_eq!(ogf(BT_HCI_CMD_INQUIRY), 0x01);
        assert_eq!(ocf(BT_HCI_CMD_INQUIRY), 0x01);
    }

    #[test]
    fn test_nop() {
        assert_eq!(BT_HCI_CMD_NOP, 0x0000);
        assert_eq!(ogf(BT_HCI_CMD_NOP), 0x00);
        assert_eq!(ocf(BT_HCI_CMD_NOP), 0x00);
    }
}
