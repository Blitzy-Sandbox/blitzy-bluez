// ============================================================================

/// Test vector containing raw EIR/AD bytes and expected parsed values.
struct TestData {
    /// Raw EIR/AD data blob.
    eir_data: &'static [u8],
    /// Expected EIR flags value.
    flags: u32,
    /// Expected device name (None = no name in EIR).
    name: Option<&'static str>,
    /// Whether the name is complete (true) or shortened (false).
    name_complete: bool,
    /// Expected TX Power Level in dBm (127 = not available).
    tx_power: i8,
    /// Expected service UUIDs in full 128-bit string format.
    uuids: &'static [&'static str],
}

