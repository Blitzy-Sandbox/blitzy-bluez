/// Verify that two UUID strings are semantically equivalent by expanding both
/// to 128-bit form and comparing bytes. This handles the short-form ("1112")
/// vs full-form ("00001112-0000-1000-8000-00805f9b34fb") difference.
fn uuid_eq(actual: &str, expected: &str) -> bool {
    let act = BtUuid::from_str(actual);
    let exp = BtUuid::from_str(expected);
    match (act, exp) {
        (Ok(a), Ok(e)) => a.to_uuid128_bytes() == e.to_uuid128_bytes(),
        _ => false,
    }
}

/// Compare two UUID lists using semantic 128-bit equality.
fn verify_uuid_list(actual: &[String], expected: &[&str]) {
    assert_eq!(
        actual.len(),
        expected.len(),
        "UUID list length mismatch: actual={actual:?}, expected={expected:?}"
    );
    for (i, (act, exp)) in actual.iter().zip(expected.iter()).enumerate() {
        assert!(uuid_eq(act, exp), "UUID mismatch at index {i}: actual='{act}', expected='{exp}'");
    }
}

/// Run the standard set of eir_parse assertions for a given test case.
fn verify_eir_parsing(test: &TestData) {
    let eir = eir_parse(test.eir_data);

    // Verify name
    match test.name {
        Some(expected_name) => {
            let actual_name = eir.name.as_deref().unwrap_or("");
            assert_eq!(
                actual_name, expected_name,
                "Name mismatch: actual='{actual_name}', expected='{expected_name}'"
            );
            assert_eq!(eir.name_complete, test.name_complete, "name_complete mismatch");
        }
        None => {
            assert!(eir.name.is_none(), "Expected no name, got: {:?}", eir.name);
        }
    }

    // Verify flags
    assert_eq!(eir.flags, test.flags, "Flags mismatch");

    // Verify TX Power Level
    assert_eq!(eir.tx_power, test.tx_power, "TX power mismatch");

    // Verify UUID list
    verify_uuid_list(&eir.services, test.uuids);
}

/// Run the BtAd cross-validation for a test case (mirrors C test_ad).
fn verify_ad(test: &TestData) {
    let eir = eir_parse(test.eir_data);
    let ad = BtAd::new_with_data(test.eir_data)
        .expect("BtAd::new_with_data should succeed for valid test data");

    // Verify flags
    assert_eq!(ad.get_flags(), test.flags as u8, "BtAd flags mismatch");

    // Verify name
    match test.name {
        Some(expected_name) => {
            let ad_name = ad.get_name();
            assert!(ad_name.is_some(), "BtAd has no name, expected: {expected_name}");
            assert_eq!(ad_name.unwrap(), expected_name, "BtAd name mismatch");
        }
        None => {
            assert!(ad.get_name().is_none(), "BtAd expected no name, got: {:?}", ad.get_name());
        }
    }

    // Verify TX Power Level
    assert_eq!(ad.get_tx_power(), test.tx_power, "BtAd TX power mismatch");

    // Verify service UUIDs
    for uuid_str in test.uuids {
        let uuid = BtUuid::from_str(uuid_str)
            .unwrap_or_else(|_| panic!("Failed to parse UUID: {uuid_str}"));
        assert!(ad.has_service_uuid(&uuid), "BtAd missing service UUID: {uuid_str}");
    }

    // Verify manufacturer data matches between EIR and BtAd
    for msd in &eir.msd_list {
        let md = ManufacturerData { manufacturer_id: msd.company, data: msd.data.clone() };
        assert!(
            ad.has_manufacturer_data(&md),
            "BtAd missing manufacturer data: company=0x{:04x}",
            msd.company
        );
    }

    // Verify service data matches between EIR and BtAd
    for sd in &eir.sd_list {
        let uuid = BtUuid::from_str(&sd.uuid)
            .unwrap_or_else(|_| panic!("Failed to parse SD UUID: {}", sd.uuid));
        let service_data = ServiceData { uuid, data: sd.data.clone() };
        assert!(
            ad.has_service_data(&service_data),
            "BtAd missing service data for UUID: {}",
            sd.uuid
        );
    }
}

